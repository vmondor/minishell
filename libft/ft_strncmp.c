#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int		flag;
	size_t	i;

	i = 0;
	flag = 0;
	while (i < n && s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	if (i < n)
		flag = (unsigned char)s1[i] - (unsigned char)s2[i];
	return (flag);
}
